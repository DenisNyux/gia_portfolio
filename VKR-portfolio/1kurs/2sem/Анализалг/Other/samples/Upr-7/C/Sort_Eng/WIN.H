#ifndef  __WIN_H
   #define  __WIN_H

   #include <conio.h>

   void win(int,int,int,int,int);
   void delBorder(int,int,int,int);
  /* -------------------------------------------- */
  /* Установка окна и рисование вокруг него рамки */
  /* -------------------------------------------- */
   void win(int x1,int y1,int x2,int y2,int tip)
   /* Устанавливает окно с координатами (x1,y1)-(x2,y2).     */
   /* tip - тип рамки.                                       */
   /* Номера битов:          Описание:                       */
   /*      0           ("толщина рамки")                     */
   /*                      0 - одинарная, 1 - двойная.       */
   /*                  ┌────────┬────────────────────┐       */
   /*    1-4           │Значение│     Цвет рамки     │       */
   /*                  ├────────┼────────────────────┤       */
   /*                  │    0   │ Светло-серый       │       */
   /*                  │    1   │ Синий              │       */
   /*                  │    2   │ Зеленый            │       */
   /*                  │    3   │ Сине-зеленый       │       */
   /*                  │    4   │ Красный            │       */
   /*                  │    5   │ Красно-синий       │       */
   /*                  │    6   │ Коричневый         │       */
   /*                  │    7   │ Светло-серый       │       */
   /*                  │    8   │ Темно-серый        │       */
   /*                  │    9   │ Ярко-синий         │       */
   /*                  │   10   │ Ярко-зеленый       │       */
   /*                  │   11   │ Яркий сине-зеленый │       */
   /*                  │   12   │ Ярко-красный       │       */
   /*                  │   13   │ Яркий красно-синий │       */
   /*                  │   14   │ Желтый             │       */
   /*                  │   15   │ Белый              │       */
   /*                  └────────┴────────────────────┘       */
   /* ------------------------------------------------------ */
   {
     char r[2][6]={{'┌', '┐', '└', '┘', '─', '│'},
                   {'╔', '╗', '╚', '╝', '═', '║'}
                  };
     int i,attr=tip>>1;
     window(x1-1,y1-1,x2+2,y2+2);
     if (attr)
       textattr(attr);
     tip&=1;
     gotoxy(1,1);        cprintf("%c",r[tip][0]);
     for (i=0; i<=x2-x1; i++)
       cprintf("%c",r[tip][4]);
     cprintf("%c",r[tip][1]);
     gotoxy(1,y2-y1+3);  cprintf("%c",r[tip][2]);
     for (i=0; i<=x2-x1; i++)
       cprintf("%c",r[tip][4]);
     cprintf("%c",r[tip][3]);
     for (i=0; i<=y2-y1; i++)
     {
       gotoxy(1,i+2);        cprintf("%c",r[tip][5]);
       gotoxy(x2-x1+3,i+2);  cprintf("%c",r[tip][5]);
     }
     if (attr)
       textattr(7);
     window(x1,y1,x2,y2);
   }
  /* --------------------------------------- */
   void delBorder(int x1,int y1,int x2,int y2)
   /* Удаляет (стирает) рамку (границу) у заданного окна */
   /* -------------------------------------------------- */
   {
     int i;
     window(x1-1,y1-1,x2+2,y2+2);
     gotoxy(1,1);
     for (i=0; i<x2-x1+3; i++)
       cprintf(" ");
     gotoxy(1,y2-y1+3);
     for (i=0; i<x2-x1+3; i++)
       cprintf(" ");
     for (i=0; i<=y2-y1; i++)
     {
       gotoxy(1,i+2);        cprintf(" ");
       gotoxy(x2-x1+3,i+2);  cprintf(" ");
     }
     window(x1,y1,x2,y2);
   }

#endif     /*  __WIN_H  */
